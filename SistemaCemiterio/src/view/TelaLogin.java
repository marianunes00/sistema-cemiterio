    private void loginUsuario(){
        //guardando/acessando o texto de login e senha digitados pelo usuario
        String loginDigitado = txtLogin.getText();
        String senhaDigitada = txtSenha.getText();

        //Cria o objeto e chama o metodo autenticar de UsuarioDao
        UsuarioDao uDao = new UsuarioDao();
        Usuario usuario = uDao.autenticar(loginDigitado, senhaDigitada);
        
        if(usuario != null){
            JOptionPane.showMessageDialog(this,"Login feito com Sucesso!");
            //Login concluido,a tela de menu é aberta e a de login é fechada
            Menu m  = new Menu(usuario);//Abre o menu já com o usuario autenticado
            m.setVisible(true);
            this.dispose();
            
        }else{
            JOptionPane.showMessageDialog(this,"Falha! Login ou senha incorretos.");
            return;
        }
    }
    
